    public void saveLogin() {

        try {
            if (!file.exists()) {
                file.createNewFile();
            }
            BufferedWriter bw = new BufferedWriter(new FileWriter(file.getAbsolutePath()));
            bw.write(txtUtilizador.getText());
            bw.close();

        } catch (IOException e) {
            e.getMessage();
        }

    }

    private void loadLogin() {

        try {
            if (file.exists()) {

                Scanner scan = new Scanner(file);
                if (scan.hasNext()) {
                    txtUtilizador.setText(scan.nextLine());
                }
                scan.close();
            }

        } catch (FileNotFoundException e) {
            e.getMessage();
        }
    }

    private void setAssociationName() {
        try {
            Association nomeAssociacao = assocController.obterAssociacao();
            lblAssociationName.setText(nomeAssociacao.getNome());
        } catch (NullPointerException ex) {
            System.out.println(ex.getMessage());
        }
    }

