    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        String username = txtUtilizador.getText();
        boolean login = usercontroller.pesquisaLogin(username, String.valueOf(pwdSenha.getPassword()));

        if (login == true) {
            app.setCurrentUser(username);
            new Main(username).setVisible(true);
            System.out.println("Login: " + username);
            this.dispose();
        } else {
            txtUtilizador.setText("");
            pwdSenha.setText("");
            lblMessage.setText("O utilizador/senha é inválido.");
        }
    }//GEN-LAST:event_btnEntrarActionPerformed

