    private void lblSocValidarMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblSocValidarMousePressed
        tabelasocios.setRowCount(0);

        ArrayList<Member> socioPesquisado = socioController.pesquisarSocio((String) jcbSocPesquisar.getSelectedItem(), txtSocPesquisar.getText(), !btnRadioSocInativos.isSelected());
        if (socioPesquisado.isEmpty()) {
            JOptionPane.showMessageDialog(this, "0 resultados encontrados para a pesquisada efetuada.", "Resultados", JOptionPane.INFORMATION_MESSAGE);
        } else {
            for (int i = 0; i < socioPesquisado.size(); i++) {
                tabelasocios.addRow(new Object[]{socioPesquisado.get(i).getNumero(), socioPesquisado.get(i).getNif(), socioPesquisado.get(i).getNome(), socioPesquisado.get(i).getTelefone(), socioPesquisado.get(i).getTelemovel(), socioPesquisado.get(i).getMorada(), socioPesquisado.get(i).getEmail(), socioPesquisado.get(i).getDatacriacao(), socioPesquisado.get(i).getDatamodif(), socioPesquisado.get(i).getUtilizador(), socioPesquisado.get(i).getCategoria()});
            }
        }
        tabelasocios.fireTableDataChanged();
    }//GEN-LAST:event_lblSocValidarMousePressed

